def test_default_jax_classic_symbols_csv_matches_symbol_tuple() -> None:
    assert DEFAULT_JAX_CLASSIC_SYMBOLS_CSV == ",".join(DEFAULT_JAX_CLASSIC_SYMBOLS)


def test_default_jax_classic_preload_points_to_expected_checkpoint() -> None:
    assert str(DEFAULT_JAX_CLASSIC_PRELOAD).endswith("unified_hourly_experiment/checkpoints/wd_0.06_s42/epoch_020.pt")


def test_compute_jax_classic_min_history_hours_makes_buffer_explicit() -> None:
    assert compute_jax_classic_min_history_hours(48, 30) == 48 + 30 * 24 + JAX_CLASSIC_MIN_HISTORY_BUFFER_HOURS
    assert compute_jax_classic_min_history_hours(32, 7, extra_history_hours=24) == 32 + 7 * 24 + 24


