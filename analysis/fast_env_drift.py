#!/usr/bin/env python3
"""Benchmark drift checker for the fast market simulator backend."""

from __future__ import annotations

import argparse
import csv
import math
from collections.abc import Iterable
from dataclasses import dataclass
from pathlib import Path


DEFAULT_MAX_REWARD_DIFF = 5e-4
DEFAULT_MAX_EQUITY_DIFF = 5e-4
DEFAULT_MAX_OBS_DIFF = 5e-4
DEFAULT_MIN_SPEEDUP = 0.95


@dataclass(slots=True)
class DriftThresholds:
    """Numerical tolerances for gap detection between Python and fast envs."""

    max_reward_diff: float = DEFAULT_MAX_REWARD_DIFF
    max_equity_diff: float = DEFAULT_MAX_EQUITY_DIFF
    max_obs_diff: float = DEFAULT_MAX_OBS_DIFF
    min_speedup: float = DEFAULT_MIN_SPEEDUP


@dataclass(slots=True)
class DriftResult:
    symbol: str
    reward_max: float
    equity_max: float
    obs_max: float
    speedup: float
    python_avg_ms: float
    fast_avg_ms: float


def parse_args(argv: Iterable[str] | None = None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Check fast env benchmark results for regressions.")
    parser.add_argument(
        "--csv",
        type=Path,
        default=Path("results/bench_fast_vs_python.csv"),
        help="Path to the benchmark CSV generated by scripts/fast_env_benchmark.py.",
    )
    parser.add_argument(
        "--symbols",
        type=str,
        default=None,
        help="Comma-separated list of symbols to inspect (default: all symbols present).",
    )
    parser.add_argument(
        "--max-reward-diff",
        type=float,
        default=DEFAULT_MAX_REWARD_DIFF,
        help="Maximum allowed absolute reward delta (default: %(default)s).",
    )
    parser.add_argument(
        "--max-equity-diff",
        type=float,
        default=DEFAULT_MAX_EQUITY_DIFF,
        help="Maximum allowed absolute equity delta (default: %(default)s).",
    )
    parser.add_argument(
        "--max-obs-diff",
        type=float,
        default=DEFAULT_MAX_OBS_DIFF,
        help="Maximum allowed observation delta (default: %(default)s).",
    )
    parser.add_argument(
        "--min-speedup",
        type=float,
        default=DEFAULT_MIN_SPEEDUP,
        help="Minimum acceptable fast-env speedup vs. Python (default: %(default)s).",
    )
    return parser.parse_args(argv)


def _read_csv(path: Path) -> list[dict]:
    if not path.exists():
        raise FileNotFoundError(f"Benchmark CSV not found: {path}")
    with path.open("r", encoding="utf-8", newline="") as handle:
        reader = csv.DictReader(handle)
        rows = list(reader)
    if not rows:
        raise ValueError(f"Benchmark CSV at {path} is empty.")
    return rows


def _to_float(raw: str | None) -> float:
    if raw is None or raw == "":
        return float("nan")
    try:
        return float(raw)
    except ValueError:
        return float("nan")


def _symbol_filter(arg: str | None) -> set[str] | None:
    if not arg:
        return None
    values = {entry.strip().upper() for entry in arg.split(",") if entry.strip()}
    return values or None


def evaluate_drift(
    rows: Iterable[dict],
    *,
    thresholds: DriftThresholds,
    symbols: set[str] | None = None,
) -> tuple[list[str], list[str], dict[str, DriftResult]]:
    deltas: dict[str, dict] = {}
    python_rows: dict[str, dict] = {}
    fast_rows: dict[str, dict] = {}
    for row in rows:
        symbol = row.get("symbol", "").strip().upper()
        if not symbol:
            continue
        if symbols and symbol not in symbols:
            continue
        backend = row.get("backend", "").strip().lower()
        if backend == "delta":
            deltas[symbol] = row
        elif backend == "python":
            python_rows[symbol] = row
        elif backend == "fast":
            fast_rows[symbol] = row

    if not deltas:
        raise ValueError("No delta rows present in benchmark CSV; rerun fast-env benchmark first.")

    errors: list[str] = []
    warnings: list[str] = []
    summaries: dict[str, DriftResult] = {}

    for symbol, delta_row in sorted(deltas.items()):
        reward_max = _to_float(delta_row.get("reward_max"))
        equity_max = _to_float(delta_row.get("equity_max"))
        obs_max = _to_float(delta_row.get("obs_max"))
        python_avg_ms = _to_float(python_rows.get(symbol, {}).get("avg_step_ms"))
        fast_avg_ms = _to_float(fast_rows.get(symbol, {}).get("avg_step_ms"))
        speedup = _to_float(delta_row.get("speedup"))
        summaries[symbol] = DriftResult(
            symbol=symbol,
            reward_max=reward_max,
            equity_max=equity_max,
            obs_max=obs_max,
            speedup=speedup,
            python_avg_ms=python_avg_ms,
            fast_avg_ms=fast_avg_ms,
        )

        if math.isnan(reward_max) or reward_max > thresholds.max_reward_diff:
            errors.append(f"{symbol}: reward delta {reward_max:.2e} exceeds limit {thresholds.max_reward_diff:.2e}")
        if math.isnan(equity_max) or equity_max > thresholds.max_equity_diff:
            errors.append(f"{symbol}: equity delta {equity_max:.2e} exceeds limit {thresholds.max_equity_diff:.2e}")
        if math.isnan(obs_max) or obs_max > thresholds.max_obs_diff:
            errors.append(f"{symbol}: observation delta {obs_max:.2e} exceeds limit {thresholds.max_obs_diff:.2e}")
        if math.isnan(speedup):
            warnings.append(
                f"{symbol}: speedup missing; recorded python/fast averages {python_avg_ms:.3f}/{fast_avg_ms:.3f} ms"
            )
        elif speedup < thresholds.min_speedup:
            errors.append(f"{symbol}: speedup {speedup:.2f}x below required {thresholds.min_speedup:.2f}x")

    return errors, warnings, summaries


def _print_summary(results: dict[str, DriftResult]) -> None:
    if not results:
        print("[warn] No symbols matched filters; nothing to report.")
        return
    header = f"{'Symbol':>6} | {'Reward Δ':>10} | {'Equity Δ':>10} | {'Obs Δ':>10} | {'Speedup':>7} | {'Py ms':>7} | {'Fast ms':>7}"
    print(header)
    print("-" * len(header))
    for symbol, data in sorted(results.items()):
        print(
            f"{symbol:>6} | "
            f"{data.reward_max:>10.2e} | "
            f"{data.equity_max:>10.2e} | "
            f"{data.obs_max:>10.2e} | "
            f"{data.speedup:>6.2f}x | "
            f"{data.python_avg_ms:>7.3f} | "
            f"{data.fast_avg_ms:>7.3f}"
        )


def main(argv: Iterable[str] | None = None) -> int:
    args = parse_args(argv)
    rows = _read_csv(args.csv)
    thresholds = DriftThresholds(
        max_reward_diff=args.max_reward_diff,
        max_equity_diff=args.max_equity_diff,
        max_obs_diff=args.max_obs_diff,
        min_speedup=args.min_speedup,
    )
    symbols = _symbol_filter(args.symbols)
    errors, warnings, summaries = evaluate_drift(rows, thresholds=thresholds, symbols=symbols)
    _print_summary(summaries)
    for warning in warnings:
        print(f"[warn] {warning}")
    if errors:
        print("[error] Fast env drift detected:")
        for error in errors:
            print(f"  - {error}")
        return 1
    print("[info] Fast env benchmark within thresholds.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
